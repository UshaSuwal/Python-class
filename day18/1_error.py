# There are broadly two types of errors in python
#1. Syntactic Error
#2. Non-Syntactic Error

# Syntactic Error
# Syntactic Errors occur when you mess-up with the grammer of the python
# Examples: Unwanted space, Indentation error, Missed commas or colons etc.


# Non-Syntactic Error
# All those errors except the syntax error are non syntactic errors
# They can further be classified as:
#1. TypeError
#2. ValueError
#3. IndexError
#4. KeyError
#5. ZeroDivisionError
#6. ModuleNotFoundError
#7. AttributeError
#8. NameError

